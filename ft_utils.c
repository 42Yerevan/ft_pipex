#include "ft_pipex.h"

void	ft_exit(char *s)
{
	ft_putstr_fd(s, 2);
	exit(2);
}

void	ft_free(char **args)
{
	int		i;

	i = 0;
	while (args[i])
	{
		free(args[i]);
		i++;
	}
}
